export function formatDateLocalized(iso: string, locale: Locale): string {
  const d = new Date(iso + "T00:00:00");
  return d.toLocaleDateString(locale === "ko" ? "ko-KR" : "en-US", {
    year: "numeric",
    month: "long",
    day: "numeric",
    weekday: "long",
  });
}
